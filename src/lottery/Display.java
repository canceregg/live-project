    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonActionPerformed
        String startString=startText.getText();
        String endString=endText.getText();
        int num;
        List<String> list=null;
        int filterType=1;
       
        String eventString =eventText.getText();
        
        try//检测数量格式
        {
            num=Integer.parseInt(numText.getText());
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "数量不是整数", "数量不是整数", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(radiobutton1.isSelected()) filterType=1;//过滤模式
        else if(radiobutton2.isSelected()) filterType=2;
        else if(radiobutton3.isSelected()) filterType=3;
        
        if(num>qq.getAllUserId().size())
        {
            JOptionPane.showMessageDialog(null, "人数大于所有人", "人数大于所有人", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(filterType==1)list=Way.create().none(qq.getAllUserId(), num);
        else if(filterType==2)list=Way.create().common(qq.getDrawList(stringToDate(startString), stringToDate(endString), eventString),qq, num);
        else if(filterType==3)list=Way.create().deep(qq.getDrawList(stringToDate(startString), stringToDate(endString), eventString),qq, num);
        
        DefaultTableModel model=(DefaultTableModel)listTable.getModel();
        int cnt=model.getRowCount();
        if(cnt>0)
        {
            for(int i=0;i<cnt;i++) model.removeRow(0);//清空上一次的名单
        }
        if(list!=null)
        {
            for(int i=0;i<list.size();i++)//更新列表
            {
                if(i<2)
                {
                    model.addRow(new Object[]{
                        eventText.getText(),list.get(i),"一等奖"
                    });
                }
                else if(i<5)
                {
                    model.addRow(new Object[]{
                        eventText.getText(),list.get(i),"二等奖"
                    });
                }
                else
                {
                    model.addRow(new Object[]{
                        eventText.getText(),list.get(i),"三等奖"
                    });
                }
            }
        }
    }//GEN-LAST:event_buttonActionPerformed

