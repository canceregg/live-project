    private Date stringToDate(String s)//字符串转化为日期
    {
        //2015-05-15 12:54:45
        int index1 = 0;
        while(s.charAt(index1) != '-')
            index1++;
        int index2 = index1+1;
        while(s.charAt(index2) != '-')
            index2++;
        int y = Integer.parseInt(s.substring(0, index1));
        int m = Integer.parseInt(s.substring(index1+1, index2));
        int index3 = index2 + 1;
        while(s.charAt(index3) != ' ')
            index3++;
        int d = Integer.parseInt(s.substring(index2+1, index3));
        int index4 = index3+1;
        while(s.charAt(index4) != ':')
            index4++;
        int h = Integer.parseInt(s.substring(index3+1, index4));
        int index5 = index4 + 1;
        while(s.charAt(index5) != ':')
            index5++;
        int min = Integer.parseInt(s.substring(index4+1, index5));
        int se = Integer.parseInt(s.substring(index5+1, s.length()));
        return new Date(y,m,d,h,min,se);
    }
    
